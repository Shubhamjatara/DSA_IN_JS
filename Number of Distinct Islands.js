/**
 * @param {number[][]} grid
 * @returns {number}
 */

class Solution {
  // Function to count the number of distinct islands.
  countDistinctIslands(grid) {
    const visited = Array.from(
      { length: grid.length },
      () => new Array(grid[0].length)
    );
    const map = new Set();
    const str = [""];
    for (let i = 0; i < grid.length; i++) {
      for (let j = 0; j < grid[0].length; j++) {
        if (!visited[i][j] && grid[i][j] == 1) {
          str[0] = `${0},${0} | `;
          this.dfs(i, j, grid, visited, str, i, j);
          map.add(str[0]);
        }
      }
    }

    return map.size;
  }

  dfs(row, col, grid, visited, str, start, end) {
    visited[row][col] = true;
    const rdel = [-1, 0, 1, 0];
    const cdel = [0, 1, 0, -1];
    for (let i = 0; i < 4; i++) {
      const nrow = row + rdel[i];
      const ncol = col + cdel[i];
      if (
        nrow >= 0 &&
        ncol >= 0 &&
        nrow < grid.length &&
        ncol < grid[0].length &&
        !visited[nrow][ncol] &&
        grid[nrow][ncol] == 1
      ) {
        str[0] = str[0] + `${nrow - start},${ncol - end} | `;
        this.dfs(nrow, ncol, grid, visited, str, start, end);
      }
    }
  }
}

